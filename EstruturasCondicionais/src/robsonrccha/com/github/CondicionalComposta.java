package robsonrccha.com.github;

public class CondicionalComposta {

	public static void main(String[] args) {
		
		// Exemplo: Verificando aluno aprovado ou não
		int nota = 6;

		if (nota >= 7) { // verifica condição
			System.out.println("Aluno Aprovado"); // se verdadeira entra no if

		} else //se condição falsa executa else
			System.out.println("Aluno Reprovado");

	}

}
